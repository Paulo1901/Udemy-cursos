# Tipo int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# possitivo ou negativo, int sem sinal é considerado positivo
# print(11) # int
# print(-11) # int
# print(0) # int

# float -> Número com pontos flutuantes 
# o tipo float representa qualquer numero
# positivo ou negativo com ponto flutuante
# float sem sinal é considerado positivo

# print(1.5, 100.78) #float
# print(0.98, -1.5) #float

